//Resumen en la Tabla 
/** 
 | Tipo      | Ejemplo         | Características                 |
| --------- | --------------- | ------------------------------- |
| String    | `"hola"`        | Texto, inmutable                |
| Number    | `42`, `3.14`    | Números, incluye NaN e Infinity |
| Boolean   | `true`, `false` | Solo dos valores                |
| Undefined | `undefined`     | Sin valor asignado              |
| Null      | `null`          | Vacío intencional               |
| BigInt    | `42n`           | Números muy grandes             |
| Symbol    | `Symbol("id")`  | Valores únicos                  |
*/